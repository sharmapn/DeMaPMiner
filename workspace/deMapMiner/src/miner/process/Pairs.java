package miner.process;

class Pair { 
	String first,second;
	
	public Pair(String f, String s){	
		this.first = f;	this.second = s;
	}
	
	public String getFirst() {			
		return first;		
	}	
	public void setFirst(String first) {	
		this.first = first;		
	}	
	public String getSecond()	{			
		return second;		
	}	
	public void setSecond(String second) {			
		this.second = second;		
	} 
};
